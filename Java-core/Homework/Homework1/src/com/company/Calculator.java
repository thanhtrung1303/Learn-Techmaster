package com.company;

public class Calculator {
    public static int sum(int a, int b) {
        return a + b;
    }
    public static int difference(int a, int b) {
        return a - b;
    }
    public static int product(int a, int b) {
        return a * b;
    }
    public static int quotient(int a, int b) {
        return a / b;
    }

}